#include <stdint.h>

class Sprite
{
public:
	Sprite(uint8_t lenX, uint8_t lenY, uint8_t *xbmA, uint8_t *xbmB) // Cons avec longueur X et Y, ImageA et ImageB
	{
		this->lenX = lenX;
		this->lenY = lenY;
		this->xbmpicA = xbmA;
		this->xbmpicB = xbmB;
	};

	Sprite(uint8_t lenX, uint8_t lenY, uint8_t *xbmA) // Cons avec longueur X et Y, ImageA seulement
	{
		this->lenX = lenX;
		this->lenY = lenY;
		this->xbmpicA = xbmA;
		this->xbmpicB = nullptr;
	};

	Sprite(uint8_t lenX, uint8_t *xbmA, uint8_t *xbmB) // Cons avec longueur X = Y, ImageA et ImageB
	{
		this->lenX = lenX;
		this->lenY = lenX;
		this->xbmpicA = xbmA;
		this->xbmpicB = xbmB;
	};

	Sprite(uint8_t lenX, uint8_t *xbmA) // Cons avec longueur X = Y, ImageA seulement
	{
		this->lenX = lenX;
		this->lenY = lenX;
		this->xbmpicA = xbmA;
		this->xbmpicB = nullptr;
	};

	uint8_t X() const
	{
		return this->lenX;
	};

	uint8_t Y() const
	{
		return this->lenY;
	};

	uint8_t *XBM(bool frame) const
	{
		if (!frame)
			return this->xbmpicA;
		else
			return this->xbmpicB;
	};

	uint8_t *XBM() const
	{
		return this->xbmpicA;
	};

private:
	uint8_t lenX = 0;
	uint8_t lenY = 0;
	uint8_t *xbmpicA = nullptr;
	uint8_t *xbmpicB = nullptr;
};

